public class VariablesYConstantes {
    public static void main(String[] args) throws Exception {
        
        // VARIABLES
        // Declaración e Iniciación de la variable edad1 en diefentes líneas:
        byte edad1;
        edad1 = 25;
        System.out.println(edad1);

        // Declaración e Iniciación de la variable edad2 en la misma línea:
        byte edad2 = 30;
        System.out.println(edad2);



        // CONSTANTES
        // Declaración e Iniciación de la constante edad3 en diefentes líneas:
        final byte edad3;
        edad3 = 50;
        System.out.println(edad3);

        // Declaración e Iniciación de la constante edad4 en la misma línea:
        final byte edad4 = 55;
        System.out.println(edad4);



        // CHAR
        char a = 'A';
        System.out.println(a);
        
    }
}

