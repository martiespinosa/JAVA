// BUCLE FOR EACH Y ARRAYS

public class Array2_BucleForEach {
    public static void main(String[] args) {
        
        String [] paises = {"España", "México", "Colombia", "Perú", "Chile", "Argentina", "Ecuador", "Venezuela"};

        for (String i: paises) {
            System.out.println(i);
        }

    }
}
